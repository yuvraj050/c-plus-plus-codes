#include <iostream>
using namespace std;
int main(){
    // data types
    int x = 5, y = 2; //int numbers
    cout<<x+y<<endl; // 7
    cout<<x-y<<endl; // 3
    cout<<x*y<<endl; // 10
    // int/int = int
    cout<<x/y<<endl; // 2 instead of 2.5 due to int data type

    float a = 10.2, b = 3; //real numbers
    cout<<a/b<<endl; //3.33 due to float data type
}